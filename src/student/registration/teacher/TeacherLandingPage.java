    private void updateBtn_marksActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtn_marksActionPerformed
        errorMessage.setText("");
        
       
        int total = 0;
        DefaultTableModel model = (DefaultTableModel) marksTable_teacher.getModel();
        if (marksTable_teacher.getSelectedRow() != -1) {
            String enrol = enrolNo_Field.getText();
            String internal = internalField.getText();
            String external = externalField.getText();
            String subcode = subjectCodeField.getText();
            if(!enrol.equals("")){
            model.setValueAt(enrol, marksTable_teacher.getSelectedRow(), 1);
            model.setValueAt(internal, marksTable_teacher.getSelectedRow(), 3);
            model.setValueAt(external, marksTable_teacher.getSelectedRow(), 4);
            model.setValueAt(subcode, marksTable_teacher.getSelectedRow(), 2);
            total = Integer.parseInt(internal) + Integer.parseInt(external);
            model.setValueAt(total, marksTable_teacher.getSelectedRow(), 5);
            new DBConnect().updateStudentMarks(enrol, internal, external, total, (total > 49), subcode);
            }else {
             errorMessage.setText("All Fields must be filled.");
            }
        } else {
            if (marksTable_teacher.getRowCount() == 0) {
                errorMessage.setText("Search for a student first.");
            } else {
                errorMessage.setText("You must select a student.");
            }
        }
    }//GEN-LAST:event_updateBtn_marksActionPerformed

