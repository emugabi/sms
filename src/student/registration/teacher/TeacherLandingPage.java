    private void addMarksBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addMarksBtnActionPerformed
        errorMessage.setText("");
        
        String enrol = enrolNo_Field.getText();
        String internal = internalField.getText();
        String external = externalField.getText();
        String subcode = subjectCodeField.getText();
        int total = Integer.parseInt(internal) + Integer.parseInt(external);
        if(!enrol.equals("") && !internal.equals("") && !external.equals("") && !subcode.equals("")){
            
            DefaultTableModel model = (DefaultTableModel) marksTable_teacher.getModel();
            model.addRow(new Object[]{model.getRowCount()+1, enrol, subcode, internal, external, total});
           
            new DBConnect().addStudentMarks( enrol, internal, external, total, (total > 50), subcode);
        }else {
            errorMessage.setText("All Fields are required!");//All Fields are required!
        }
    }//GEN-LAST:event_addMarksBtnActionPerformed

